function addTicketPhoto(ticketId) {
  // Create a hidden file input
  var input = document.createElement('input');
  input.type = 'file';
  input.accept = 'image/*';
  input.multiple = true;
  input.onchange = function() {
    if (!input.files || !input.files.length) return;
    if (!TICKET_PHOTOS[ticketId]) TICKET_PHOTOS[ticketId] = [];
    for (var i = 0; i < input.files.length; i++) {
      var file = input.files[i];
      // Store the file name (in production: upload to server and store URL)
      TICKET_PHOTOS[ticketId].push(file.name);
    }
    saveData();
    toast(input.files.length + ' photo(s) attached to ' + ticketId, 'success');
    pushNotif('fa-camera', '#FD79A8', 'Photo Attached', input.files.length + ' photo(s) added to ' + ticketId);
    closeModal();
    setTimeout(function() { showTicketPhotos(ticketId); }, 200);
  };
  input.click();
}

