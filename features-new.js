function generateUtilityBills() {
  const now = new Date();
  const monthNames = ['January','February','March','April','May','June','July','August','September','October','November','December'];
  const month = monthNames[now.getMonth()];
  const year = now.getFullYear();
  const billingPeriod = month + ' ' + year;

  // Check if already generated for this month
  const existingBill = UTILITY_BILLS.find(b => b.period === billingPeriod);
  if (existingBill) {
    toast('Utility bills already generated for ' + billingPeriod, 'info');
    showUtilityBillList();
    return;
  }

  let count = 0;

  TENANTS.forEach(t => {
    if (t.s !== 'active') return;

    const propName = t.p.split(' ')[0];

    // Electric meter reading
    const elecMeter = ELECTRIC_METERS.find(m => m.tenant === t.n);
    const elecKwh = elecMeter ? elecMeter.kwh : 0;
    const elecAmt = Math.round(elecKwh * UTILITY_RATES.electric.rate * 100) / 100;

    // Water meter reading
    const waterMeter = WATER_METERS.find(m => m.tenant === t.n);
    const waterM3 = waterMeter ? waterMeter.m3 : 0;
    const waterAmt = Math.round(waterM3 * UTILITY_RATES.water.rate * 100) / 100;

    // Internet (flat rate per room)
    const internetAmt = UTILITY_RATES.internet.rate;

    // Sewerage (flat rate)
    const sewerageAmt = UTILITY_RATES.sewerage.rate;

    const totalAmt = Math.round((elecAmt + waterAmt + internetAmt + sewerageAmt) * 100) / 100;

    const billId = 'UB-' + year + String(now.getMonth() + 1).padStart(2, '0') + '-' + String(UTILITY_BILLS.length + count + 1).padStart(3, '0');

    UTILITY_BILLS.push({
      id: billId,
      tenant: t.n,
      unit: t.p,
      property: propName,
      period: billingPeriod,
      date: now.toISOString().slice(0, 10),
      items: [
        { type: 'Electricity', usage: elecKwh, unit: 'kWh', rate: UTILITY_RATES.electric.rate, amount: elecAmt, meterId: elecMeter ? elecMeter.meterId : 'N/A' },
        { type: 'Water', usage: waterM3, unit: 'm\u00B3', rate: UTILITY_RATES.water.rate, amount: waterAmt, meterId: waterMeter ? waterMeter.meterId : 'N/A' },
        { type: 'Internet', usage: 1, unit: 'month', rate: UTILITY_RATES.internet.rate, amount: internetAmt, meterId: 'Flat' },
        { type: 'Sewerage', usage: 1, unit: 'month', rate: UTILITY_RATES.sewerage.rate, amount: sewerageAmt, meterId: 'Flat' }
      ],
      total: totalAmt,
      status: 'Pending'
    });
    count++;
  });

  saveData();
  toast(count + ' utility bill(s) generated for ' + billingPeriod, 'success');
  pushNotif('fa-file-invoice', '#74B9FF', 'Utility Bills Generated', count + ' bills for ' + billingPeriod);
  showUtilityBillList();
}

