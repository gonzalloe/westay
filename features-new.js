function doCreateCheckInOut() {
  var tenantName = document.getElementById('cioTenant').value;
  var tenant = TENANTS.find(function(t) { return t.n === tenantName; });
  if (!tenant) { toast('Select a tenant', 'error'); return; }

  var rec = {
    id: 'CIO-' + String(CHECKINOUT_RECORDS.length + 1).padStart(3, '0'),
    tenant: tenant.n,
    unit: tenant.p,
    type: document.getElementById('cioType').value,
    date: document.getElementById('cioDate').value || new Date().toISOString().slice(0, 10),
    inspector: sanitizeInput(document.getElementById('cioInspector').value) || 'Admin Operator',
    status: 'In Progress',
    notes: sanitizeInput(document.getElementById('cioNotes').value) || '',
    photos: []
  };

  CHECKINOUT_RECORDS.push(rec);
  saveData();
  toast(rec.type.replace('-', ' ').toUpperCase() + ' record created for ' + escHtml(tenant.n), 'success');
  pushNotif('fa-clipboard-check', '#00CEC9', rec.type.toUpperCase(), escHtml(tenant.n) + ' — ' + escHtml(tenant.p));

  closeModal();
  setTimeout(function() { showCheckInOutDetail(rec.id); }, 200);
}

function showCheckInOutDetail(recId) {
  var rec = CHECKINOUT_RECORDS.find(function(r) { return r.id === recId; });
  if (!rec) { toast('Record not found', 'error'); return; }

  var html = '<div class="dep-card">' +
    depRow('ID', escHtml(rec.id)) +
    depRow('Tenant', escHtml(rec.tenant)) +
    depRow('Unit', escHtml(rec.unit)) +
    depRowHtml('Type', '<span class="bs ' + (rec.type === 'check-in' ? 'b-info' : 'b-warn') + '">' + escHtml(rec.type.toUpperCase()) + '</span>') +
    depRow('Date', escHtml(rec.date)) +
    depRow('Inspector', escHtml(rec.inspector || 'N/A')) +
    depRowHtml('Status', '<span class="bs ' + (rec.status === 'Completed' ? 'b-ok' : 'b-warn') + '">' + escHtml(rec.status) + '</span>') +
    '</div>';

  if (rec.notes) {
    html += '<div style="margin:12px 0"><div style="font-size:11px;font-weight:600;margin-bottom:6px;color:var(--t2)"><i class="fas fa-sticky-note" style="color:var(--warn);margin-right:6px"></i>Notes</div>' +
      '<div style="padding:10px;background:var(--bg3);border-radius:10px;font-size:12px;color:var(--t2);line-height:1.5">' + escHtml(rec.notes) + '</div></div>';
  }

  // Photos section
  html += '<div style="margin:12px 0"><div style="font-size:11px;font-weight:600;margin-bottom:8px;color:var(--t2)"><i class="fas fa-camera" style="color:#FD79A8;margin-right:6px"></i>Photos (' + (rec.photos ? rec.photos.length : 0) + ')</div>';

  if (rec.photos && rec.photos.length) {
    html += '<div style="display:grid;grid-template-columns:repeat(auto-fill,minmax(120px,1fr));gap:8px">';
    rec.photos.forEach(function(photo, i) {
      var color = COLORS[i % 8];
      html += '<div style="position:relative;background:' + color + '22;border:1px solid ' + color + '44;border-radius:10px;padding:20px 8px;text-align:center;cursor:pointer" onclick="viewPhoto(\'' + escHtml(photo) + '\')">' +
        '<i class="fas fa-image" style="font-size:22px;color:' + color + ';display:block;margin-bottom:6px"></i>' +
        '<div style="font-size:9px;color:var(--t3);word-break:break-all">' + escHtml(photo) + '</div>' +
        '<button class="btn-s" style="position:absolute;top:4px;right:4px;font-size:8px;padding:2px 5px" onclick="event.stopPropagation();removeCioPhoto(\'' + recId + '\',' + i + ')"><i class="fas fa-times"></i></button></div>';
    });
    html += '</div>';
  } else {
    html += '<div style="text-align:center;padding:16px;background:var(--bg3);border-radius:10px;color:var(--t3);font-size:11px"><i class="fas fa-camera" style="display:block;font-size:20px;margin-bottom:6px"></i>No photos yet — add photos to document the unit condition</div>';
  }
  html += '</div>';

  // Actions
  html += '<div style="display:grid;grid-template-columns:1fr 1fr;gap:8px;margin-top:14px">';
  html += '<button class="btn btn-p" onclick="addCioPhoto(\'' + recId + '\')"><i class="fas fa-camera"></i> Add Photos</button>';
  if (rec.status !== 'Completed') {
    html += '<button class="btn btn-ok" onclick="completeCio(\'' + recId + '\')"><i class="fas fa-check"></i> Mark Complete</button>';
  } else {
    html += '<button class="btn btn-ghost" onclick="closeModal();showCheckInOutList()"><i class="fas fa-arrow-left"></i> Back to List</button>';
  }
  html += '</div>';

  closeModal();
  setTimeout(function() {
    openModal('<i class="fas fa-clipboard-check" style="color:#00CEC9"></i> ' + escHtml(rec.type.toUpperCase()) + ' — ' + escHtml(rec.tenant), html,
      '<button class="btn btn-ghost" onclick="closeModal();showCheckInOutList()">Back</button>', 'sm');
  }, 200);
}

function addCioPhoto(recId) {
  var rec = CHECKINOUT_RECORDS.find(function(r) { return r.id === recId; });
  if (!rec) return;
  var input = document.createElement('input');
  input.type = 'file';
  input.accept = 'image/*';
  input.multiple = true;
  input.onchange = function() {
    if (!input.files || !input.files.length) return;
    if (!rec.photos) rec.photos = [];
    for (var i = 0; i < input.files.length; i++) {
      rec.photos.push(input.files[i].name);
    }
    saveData();
    toast(input.files.length + ' photo(s) added to ' + escHtml(rec.id), 'success');
    closeModal();
    setTimeout(function() { showCheckInOutDetail(recId); }, 200);
  };
  input.click();
}

function removeCioPhoto(recId, index) {
  confirmDialog('Remove Photo?', 'Remove this photo from the inspection record?', function() {
    var rec = CHECKINOUT_RECORDS.find(function(r) { return r.id === recId; });
    if (rec && rec.photos) {
      rec.photos.splice(index, 1);
      saveData();
      toast('Photo removed', 'info');
      closeModal();
      setTimeout(function() { showCheckInOutDetail(recId); }, 200);
    }
  }, 'danger');
}

function completeCio(recId) {
  confirmDialog('Complete Inspection?', 'Mark this check-in/check-out inspection as completed?', function() {
    var rec = CHECKINOUT_RECORDS.find(function(r) { return r.id === recId; });
    if (rec) {
      rec.status = 'Completed';
      saveData();
      toast('Inspection ' + escHtml(rec.id) + ' completed!', 'success');
      closeModal();
      setTimeout(function() { showCheckInOutDetail(recId); }, 200);
    }
  }, 'success');
}
